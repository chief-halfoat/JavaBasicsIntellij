package com.syntax.class07;

public class Homework03 {

	public static void main(String[] args) {

		// Print even numbers from 20 to 1 (2 ways)

		int j = 20;

		while (j >= 1) {
			if (j % 2 == 0) {
				System.out.println(j);
			}
			j--;
		}

	
	int k = 20;
	
	while (k>=1) {
		System.out.println(k);
		k-=2;
		
	}
	
	
	}

}
