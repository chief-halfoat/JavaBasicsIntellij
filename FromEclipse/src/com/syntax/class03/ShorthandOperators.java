package com.syntax.class03;

public class ShorthandOperators {

	public static void main(String[] args) {

		int num=100;
		num=num+100;
		System.out.println(num);

		num += 100; // 300
		System.out.println(num);
		num -= 20; // 280
		System.out.println(num);
		num /= 10;
		System.out.println(num);
		num%=2;
		System.out.println(num);
	
	}	
}
